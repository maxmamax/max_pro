    <div class="alert alert-success alert-dismissible" style="margin-top:20px;font-size:20px;">
      <p style="color:#26519e;"><strong>สหกิจศึกษาคืออะไร ?</strong></p>
    </div>
<p style="text-indent:23px; text-align:justify; font-size:20px;">สหกิจศึกษา (Cooperative Education) เป็นระบบการศึกษาที่จัดให้มีการเรียนการสอนในสถานศึกษาสลับกับการไปหาประสบการณ์ตรงจากการปฏิบัติงานจริง ณ สถานประกอบการอย่างเป็นระบบ ด้วยความร่วมมือจากสถานประกอบการและทุกฝ่ายที่เกี่ยวข้อง ซึ่งลักษณะงานที่นักศึกษาได้รับมอบหมายให้ปฏิบัติตรงกับสาขาวิชาของนักศึกษา และการปฏิบัติงานของนักศึกษาเสมือนพนักงานคนหนึ่งของสถานประกอบการ สหกิจศึกษาจึงเป็นระบบการศึกษาที่ผสมผสานการเรียนกับการปฏิบัติงาน (Work Integrated Learning) รูปแบบการจัดการศึกษาสหกิจศึกษาจึงมุ่งเน้นให้นักศึกษาได้มีโอกาสปฏิบัติงานจริงในสถานประกอบการสลับกับการเรียนทฤษฎีในห้องเรียนก่อนที่จะสำเร็จการศึกษาและออกไปประกอบวิชาชีพ เป็นการเตรียมความพร้อมให้แก่นักศึกษาด้านการพัฒนาอาชีพ (Career Development) เสริมทักษะประสบการณ์ให้พร้อมที่จะเข้าสู่ระบบการทำงาน (Employability) และเสริมคุณภาพบัณฑิตให้มีคุณภาพตรงตามที่สถานประกอบการต้องการ นอกจากนั้นสหกิจศึกษายังถือเป็นกลไกความร่วมมือทางวิชาการระหว่างสถาบันการศึกษาและสถานประกอบการอีกด้วย</p>

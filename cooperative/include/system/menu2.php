<div class="alert alert-success alert-dismissible" style="margin-top:20px;font-size:20px;">
  <p style="color:#26519e;"><strong>ลักษณะการปฏิบัติงานสหกิจศึกษา ?</strong></p>
</div>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt;">1. นักศึกษาปฏิบัติงานเสมือนเป็นพนักงานชั่วคราวของสถานประกอบการ โดยมีพนักงานที่ปรึกษาทำหน้าที่ให้คำแนะนำและดูแลตลอดระยะเวลาการปฏิบัติงาน</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt;">2. นักศึกษามีหน้าที่รับผิดชอบที่แน่นอนด้วยงานที่ได้รับมอบหมายจากสถานประกอบการ ซึ่งตรงตามสาขาวิชาที่ได้ศึกษามา</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt;">3. นักศึกษาปฏิบัติงานเต็มเวลา (Full Time) ระยะเวลา 1 ภาคการศึกษา (16 สัปดาห์) โดยมีอาจารย์ที่ปรึกษาสหกิจศึกษาทำหน้าที่ให้คำปรึกษาและดูแลเอาใจใส่อย่างใกล้ชิด</p>
<div class="alert alert-success alert-dismissible" style="margin-top:20px;font-size:20px;">
  <p style="color:#26519e;"><strong>คุณสมบัติของนักศึกษาสหกิจศึกษา ?</strong></p>
</div>

<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">1. ควรมีคะแนนเฉลี่ยสะสม 2.00 ขึ้นไป</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">2. เรียนรายวิชาบังคับตามที่กำหนด</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">3. มีความประพฤติเรียบร้อย มีความรับผิดชอบ มีวุฒิภาวะ</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">4. สามารถพัฒนาตนเองได้</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">5. มีบุคลิกภาพสอดคล้องกับงานที่ต้องทำในสถานประกอบการ้</p>

<div class="alert alert-success alert-dismissible" style="margin-top:20px;font-size:20px;">
  <p style="color:#26519e;"><strong>ประโยชน์ที่นักศึกษาจะได้รับจากการเข้าร่วมสหกิจศึกษา ?</strong></p>
</div>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">1. ได้ประสบการณ์วิชาชีพตรงตามสาขาวิชา</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">2. ได้เรียนรู้สังคมการทำงาน และการทำงานร่วมกับทีมงาน</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">3. ค้นพบตนเองและเข้าใจศักยภาพความต้องการของตนเองในสายงานวิชาชีพ</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">4. เกิดการพัฒนาตนเอง และมีความเชื่อมั่นในตนเองมากขึ้น</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">5. เกิดทักษะการสื่อสาร การประสานงาน และการรายงานข้อมูล</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">6. มีผลการเรียนหลังการปฏิบัติงานสหกิจศึกษาดีขึ้น</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">7. เป็นบัณฑิตที่มีศักยภาพและความพร้อมปฏิบัติงานได้ทันทีที่สำเร็จการศึกษา</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">8. มีโอกาสได้รับการเสนองานก่อนสำเร็จการศึกษา</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">9. ได้รับค่าตอบแทนขณะศึกษา</p>

<div class="alert alert-success alert-dismissible" style="margin-top:20px;font-size:20px;">
  <p style="color:#26519e;"><strong>ประโยชน์ที่สถานประกอบการได้รับ ?</strong></p>
</div>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">1. มีนักศึกษาช่วยปฏิบัติงานตลอดปี</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">2. พนักงานประจำมีเวลามากขึ้นในการปฏิบัติงานในหน้าที่อื่นหรือสร้างสรรค์งานที่มีคุณค่าให้กับองค์กร</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">3. เป็นช่องทางการสรรหาบุคคลเข้าเป็นพนักงานประจำ</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">4. มีโอกาสสร้างความร่วมมือทางวิชาการกับสถานศึกษา</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">5. เกิดภาพพจน์ที่ดีด้านการส่งเสริมการศึกษา</p>

<div class="alert alert-success alert-dismissible" style="margin-top:20px;font-size:20px;">
  <p style="color:#26519e;"><strong>ประโยชน์ที่สถานประกอบการได้รับ ?</strong></p>
</div>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">1. เกิดความร่วมมือทางวิชาการและความสัมพันธ์ที่ดีกับสถานประกอบการ</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">2. ได้ข้อมูลย้อนกลับสำหรับปรับปรุงหลักสูตรและการเรียนการสอน</p>
<p style="font-size:18px; text-indent:23px; text-align:justify; line-height: 18pt; ">3. ช่วยให้สถานศึกษาเป็นที่รู้จักและได้รับการยอมรับจากตลาดแรงงาน</p>
